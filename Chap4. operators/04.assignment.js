// 할당연산자 (Assignment Operators)
let a = 1;
console.log(a);

a = a + 2;
console.log(a);

// 산술연산자와 할당 연산자를 합쳐서 사용할 수 있다.
a += 2; // a = a + 2;
console.log(a);

a -= 2; // a = a - 2;
console.log(a);

a *= 2; // a = a * 2;
console.log(a);

a /= 2; // a = a / 2;
console.log(a);

a %= 2; // a = a % 2;
console.log(a);

a **= 2; // a = a ** 2;
console.log(a);
